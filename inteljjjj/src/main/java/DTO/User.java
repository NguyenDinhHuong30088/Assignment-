package DTO;

/**
 *
 * @author crrtt
 */
public class User {
    int userID, roleID, status;
    String fullName, password, address, birthday, phone, mail;

    public User() {
    }

    public User(int userID, int roleID, int status, String fullName, String password, String address, String birthday, String phone, String mail) {
        this.userID = userID;
        this.roleID = roleID;
        this.status = status;
        this.fullName = fullName;
        this.password = password;
        this.address = address;
        this.birthday = birthday;
        this.phone = phone;
        this.mail = mail;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getRoleID() {
        return roleID;
    }

    public void setRoleID(int roleID) {
        this.roleID = roleID;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }
    
    
    
}
